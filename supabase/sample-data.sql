- **工厂模式** - 创建对象而不指定具体类
- **建造者模式** - 分步骤构建复杂对象

### 结构型模式
- **适配器模式** - 让不兼容的接口协同工作
- **装饰器模式** - 动态添加功能
- **外观模式** - 提供简化的接口

